/**
 * Glossary of mathematical notation used across the portal.
 *
 * Each entry is keyed by a short stable identifier (used as the URL anchor
 * on /glossary and as the `key` prop of the <Term> wrapper component).
 *
 * Style:
 * - `term`: human-readable name. Plain text only — appears in the tooltip header.
 * - `symbol`: optional KaTeX source for the canonical symbol (rendered on the
 *   /glossary page; not in the tooltip).
 * - `definition`: plain text, 1–3 sentences. Appears in the tooltip and on
 *   the /glossary page. NO KaTeX in the tooltip — use unicode (ℤ, ℝ, ℂ, etc.)
 *   or describe in words.
 * - `example`: optional plain-text example, also tooltip-safe.
 *
 * Add new entries here when authoring content; reference them via
 * `<Term key="..."> ... </Term>` in MDX.
 */

export interface GlossaryEntry {
  term: string;
  symbol?: string;
  definition: string;
  example?: string;
}

